         <h2>Veille Technologique</h2><br>
         <p>
             La veille technologique, élément de la veille stratégique, consiste à surveiller les évolutions techniques, les innovations dans un secteur d’activité donné. La veille technologique comprend notamment la surveillance, la collecte, le partage et la diffusion d’information permettant d’anticiper ou de s’informer sur des changements en matières de Recherche, Développement, Brevet, Lancement de nouveaux produits, matériaux, processus, concepts, innovation de fabrication…afin d’évaluer l’impact sur l’environnement et l’organisation.
             La veille technologique impliquera de surveiller et d’analyser notamment l’information scientifique, technique, mais aussi les tendances et analyses prospectives afin de détecter au mieux les cycles et les ruptures.
